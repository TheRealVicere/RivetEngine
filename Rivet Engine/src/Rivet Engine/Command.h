#ifndef Command_H
#define Command_H

////////////////////////////////////////////////////////////////////////////////////////////////////
/// \class	Command
///
/// \brief	A base command for the command pattern.
///
/// \author	Mike Piunti
/// \date	02/28/2023
////////////////////////////////////////////////////////////////////////////////////////////////////

class Command {
public:
	Command() = default;
	Command(const Command&) = delete;
	Command& operator=(const Command&) = delete;
	virtual ~Command() = default;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// \fn	virtual void Command::Execute() = 0;
	///
	/// \brief	Executes the command associated with this object.
	////////////////////////////////////////////////////////////////////////////////////////////////////
	virtual void Execute() = 0;
};

#endif
