#ifndef IAlarmCallback_H
#define IAlarmCallback_H

////////////////////////////////////////////////////////////////////////////////////////////////////
/// \class	IAlarmCallback
///
/// \brief	A callback to perform the duties of an alarm component.
///
/// \author	Mike Piunti
/// \date	04/26/2023
////////////////////////////////////////////////////////////////////////////////////////////////////
class IAlarmCallback {
public:
	IAlarmCallback() = default;
	IAlarmCallback(const IAlarmCallback&) = delete;
	IAlarmCallback& operator=(const IAlarmCallback&) = delete;
	virtual ~IAlarmCallback() = default;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// \fn	virtual void IAlarmCallback::Alarm() = 0;
	///
	/// \brief	Handles actions for an alarm.
	////////////////////////////////////////////////////////////////////////////////////////////////////
	virtual void Alarm() = 0;
};

#endif
