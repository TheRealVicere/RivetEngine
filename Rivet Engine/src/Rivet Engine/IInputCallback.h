#ifndef IInputCallback_H
#define IInputCallback_H

////////////////////////////////////////////////////////////////////////////////////////////////////
/// \class	IInputCallback
///
/// \brief	A callback to perform the duties of an input component.
///
/// \author	Mike Piunti
/// \date	04/26/2023
////////////////////////////////////////////////////////////////////////////////////////////////////
class IInputCallback {
public:
	IInputCallback() = default;
	IInputCallback(const IInputCallback&) = delete;
	IInputCallback& operator=(const IInputCallback&) = delete;
	virtual ~IInputCallback() = default;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// \fn	virtual void IInputCallback::KeyEvent() = 0;
	///
	/// \brief	Handles actions for a key event.
	////////////////////////////////////////////////////////////////////////////////////////////////////
	virtual void KeyEvent() = 0;
};

#endif
