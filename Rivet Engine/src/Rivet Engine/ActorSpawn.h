#ifndef ActorSpawn_H
#define ActorSpawn_H

#include "ISpawnCallback.h"

class Actor;

////////////////////////////////////////////////////////////////////////////////////////////////////
/// \class	ActorSpawn
///
/// \brief	A generic spawn callback for actors.
///
/// \author	Mike Piunti
/// \date	05/23/2023
////////////////////////////////////////////////////////////////////////////////////////////////////
class ActorSpawn : public ISpawnCallback {
public:
	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// \fn	ActorSpawn::ActorSpawn(Actor* a);
	///
	/// \brief	Creates a spawn callback for the given actor.
	///
	/// \param	a	The Actor to spawn.
	////////////////////////////////////////////////////////////////////////////////////////////////////
	ActorSpawn(Actor* a);

	ActorSpawn() = delete;
	ActorSpawn(const ActorSpawn&) = delete;
	ActorSpawn& operator=(const ActorSpawn&) = delete;
	~ActorSpawn() = default;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// \fn	virtual void ActorSpawn::EnterScene() override;
	///
	/// \brief	Makes the stored actor enter the scene.
	////////////////////////////////////////////////////////////////////////////////////////////////////
	void EnterScene() override;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// \fn	virtual void ActorSpawn::ExitScene() override;
	///
	/// \brief	Makes the stored actor exit the scene.
	////////////////////////////////////////////////////////////////////////////////////////////////////
	void ExitScene() override;

private:
	/// \brief	The actor to spawn.
	Actor* actor;
};

#endif
